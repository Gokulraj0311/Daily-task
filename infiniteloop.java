public class infiniteloop {
    public static void main(String[] args) {
        System.out.println("For loop Enters...!");
       for(int i=0;i>0;i++){
        System.out.println("This loop runs 100 times");
       }
       System.out.println("entry of Infinite while Loop...!");
       int j=0;
       while(j>0){
        System.out.println("This while runs forever..!");
       }
    }
}
